package Article;

public class Article {

    private int id;
    private String name;
    private float price;

    public int getId() {
        return id;
    }
    public int setId(int id) {
        this.id = id;
        return id;
    }

    public String getName() {
        return name;
    }
    public String setName(String name) {
        this.name = name;
        return name;
    }

    public float getPrice() {
        return price;
    }
    public float setPrice(float price) {
        this.price = price;
        return price;
    }
}
